package id.raflyfirdausy.skype;

public class    RiwayatModel {
    private String id_riwayat, id_data, masuk_keluar, waktu, penduduk_tamu, nama, alamat, ktp;

    public RiwayatModel() {
    }

    public String getId_riwayat() {
        return id_riwayat;
    }

    public void setId_riwayat(String id_riwayat) {
        this.id_riwayat = id_riwayat;
    }

    public String getId_data() {
        return id_data;
    }

    public void setId_data(String id_data) {
        this.id_data = id_data;
    }

    public String getMasuk_keluar() {
        return masuk_keluar;
    }

    public void setMasuk_keluar(String masuk_keluar) {
        this.masuk_keluar = masuk_keluar;
    }

    public String getWaktu() {
        return waktu;
    }

    public void setWaktu(String waktu) {
        this.waktu = waktu;
    }

    public String getPenduduk_tamu() {
        return penduduk_tamu;
    }

    public void setPenduduk_tamu(String penduduk_tamu) {
        this.penduduk_tamu = penduduk_tamu;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getAlamat() {
        return alamat;
    }

    public void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    public String getKtp() {
        return ktp;
    }

    public void setKtp(String ktp) {
        this.ktp = ktp;
    }
}
